bool
digest_testsuite_run(void)
{
	(void) slog(LG_DEBUG, "%s: running", __func__);

	if (! digest_testsuite_run_md5())
		return false;

	if (! digest_testsuite_run_hmac_md5())
		return false;

	if (! digest_testsuite_run_pbkdf2_hmac_md5())
		return false;

	(void) slog(LG_DEBUG, "%s: passed", __func__);

	return true;
}
